"""新旧颜色迁移算法的固定多峰配色回归样例。"""

import numpy as np

PALETTE_PAIRS = (
    ([[30, 200, 200], [220, 40, 40]], [[30, 40, 220], [220, 200, 30]]),
    ([[20, 100, 230], [230, 150, 20], [80, 220, 50]], [[230, 60, 130], [40, 210, 210], [180, 190, 30]]),
    ([[84, 237, 205], [78, 107, 154], [240, 227, 80], [83, 244, 15]], [[130, 80, 100], [197, 210, 107], [77, 104, 55], [40, 236, 109]]),
    ([[20, 180, 80], [180, 20, 80], [80, 80, 220]], [[220, 80, 80], [80, 220, 80], [80, 80, 20]]),
    ([[171, 78, 67], [151, 209, 60], [82, 188, 24], [90, 193, 197]], [[107, 57, 172], [64, 134, 54], [73, 78, 137], [54, 223, 105]]),
)


def palette_image(colors: list[list[int]], repeats: int = 300) -> np.ndarray:
    """把固定调色板扩展为带自然微变化的测试图片。"""
    pixels = np.tile(np.asarray(colors, dtype=np.uint8), (repeats, 1))
    jitter = np.arange(pixels.shape[0], dtype=np.int16)[:, None] % 13 - 6
    pixels = np.clip(pixels.astype(np.int16) + jitter, 0, 255).astype(np.uint8)
    height = 30
    return pixels.reshape(height, -1, 3)
